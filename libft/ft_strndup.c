#include "libft.h"

char	*ft_strndup(const char *s1, int len)
{
	int		j;
	int		i;
	char	*str;

	i = 0;
	j = ft_strlen(s1);
	if (len < 0 || len > j)
		return (NULL);
	if (!(str = (char *)malloc(sizeof(char) * (len + 1))))
		return (NULL);
	while (i < len)
	{
		str[i] = s1[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
